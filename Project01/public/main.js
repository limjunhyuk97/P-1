const containerEl = document.querySelector("#container");
const toolbarEl = containerEl.querySelector(".toolbar");
const menukeyEl = toolbarEl.querySelector(".menu-key");

const contentsEl = containerEl.querySelector(".contents");
const slidemenuEl = contentsEl.querySelector(".slide-menu");

// Frame 1 띄우기

menukeyEl.addEventListener('mouseover', function(){
  slidemenuEl.classList.add('focused');
});

menukeyEl.addEventListener('mouseout', function(){
  if(!slidemenuEl.classList.contains('.clicked'))
    slidemenuEl.classList.remove('focused');
});

menukeyEl.addEventListener('click', function(){
  if(slidemenuEl.classList.contains('.clicked')){
    slidemenuEl.classList.remove('.clicked');
    console.log("hi");
  }
  else{
    slidemenuEl.classList.add('.clicked');
  }
});